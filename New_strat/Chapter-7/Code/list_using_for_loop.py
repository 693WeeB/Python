# write a program to print the item inside of the list using for loop

# now do with list
list = ["list1", "list2", "list3", "list4", "list5"]
print(type(list))

for i in list:
    print(i)

# now do with set
S = {
    "set1",
    "set2",
    "set3",
    "set4",
    "set5"
}
print(type(S))
for i in S:
    print(i)


# now do with tuple
t = (
    "tuple1",
    "tuple2",
    "tuple3",
    "tuple4",
    "tuple5"
)
print(type(t))

for i in t:
    print(i)