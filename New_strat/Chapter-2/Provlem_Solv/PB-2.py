#____________Arithmetic_Operations_________

x = 29
y = 6

#Remaining code for arithmetic operations
print("Remainder of", x, "divided by", y, "remainder is = ", x % y)  # Modulus

# Exponentiation
print("Exponentiation of", x, "to the power of", y, "exponentiation is = ", x ** y)  # Exponentiation

# Floor Division
print("Floor division of", x, "by", y, "floor division is = ", x // y)  # Floor Division